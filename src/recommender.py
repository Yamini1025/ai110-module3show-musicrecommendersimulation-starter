def load_songs(csv_path: str) -> List[Dict]:
    """
    Loads songs from a CSV file.
    Required by src/main.py
    """
    # Numerical columns and the type each should be converted to.
    int_fields = {"id", "tempo_bpm"}
    float_fields = {"energy", "valence", "danceability", "acousticness"}

    songs: List[Dict] = []
    with open(csv_path, newline="", encoding="utf-8") as f:
        # DictReader uses the header row as keys, so each row is a dict.
        reader = csv.DictReader(f)
        for row in reader:
            for field in int_fields:
                row[field] = int(row[field])
            for field in float_fields:
                row[field] = float(row[field])
            songs.append(row)
    return songs

def score_song(user_prefs: Dict, song: Dict) -> Tuple[float, List[str]]:
    """
    Scores a single song against user preferences.
    Required by recommend_songs() and src/main.py
    """
    score = 0.0
    reasons: List[str] = []

    # Genre match: +5 points
    if song["genre"] == user_prefs["genre"]:
        score += 5
        reasons.append("genre match (+5)")

    # Mood match: +3 points
    if song["mood"] == user_prefs["mood"]:
        score += 3
        reasons.append("mood match (+3)")

    # Energy similarity: up to +2 points.
    # energy_score is 1 when the energies are identical and drops toward 0
    # as they get further apart, then we scale it to a max of 2 points.
    energy_score = 1 - abs(song["energy"] - user_prefs["energy"])
    energy_points = energy_score * 2
    score += energy_points
    reasons.append(f"energy similarity (+{energy_points:.1f})")

    return score, reasons

def recommend_songs(user_prefs: Dict, songs: List[Dict], k: int = 5) -> List[Tuple[Dict, float, str]]:
    """
    Functional implementation of the recommendation logic.
    Required by src/main.py
    """
    # Score every song and build a (song, score, explanation) tuple for each.
    scored = []
    for song in songs:
        score, reasons = score_song(user_prefs, song)
        explanation = ", ".join(reasons)
        scored.append((song, score, explanation))

    # Sort from highest score to lowest. The score is index 1 of each tuple.
    scored_sorted = sorted(scored, key=lambda item: item[1], reverse=True)

    # Return only the top k.
    return scored_sorted[:k]
